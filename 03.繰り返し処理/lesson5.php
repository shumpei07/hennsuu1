<?php
// 以下をfor文を使用して表示してください。
// ヒント: forの中でforを３回使用

// ********1
// *******121
// ******12321
// *****1234321
// ****123454321
// ***12345654321
// **1234567654321
// *123456787654321
// 12345678987654321

$rows = 9; // 行数を指定する変数

for ($i = 1; $i <= $rows; $i++) {
    // 空白を出力
    for ($j = 1; $j <= $rows - $i; $j++) {
        echo " ";
    }

    // 左半分の数字を出力
    for ($k = 1; $k <= $i; $k++) {
        echo $k;
    }

    // 右半分の数字を逆順に出力
    for ($l = $i - 1; $l >= 1; $l--) {
        echo $l;
    }

    echo "<br/>";
}